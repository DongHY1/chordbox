export function getIndexToString(num:number):string{
    let res:string = ''
    switch(num){
        case 1:
            res = 'one'
            break;
        case 2:
            res = 'two'
            break;
        case 3:
            res = 'three'
            break;
        case 4:
            res = 'four'
            break;
        case 5:
            res = 'five'
            break;
        case 6:
            res = 'six'
            break;
    }
    return res
    
}